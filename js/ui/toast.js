function toast(msg, type, duration, icon) {
  type     = type     || 'info';
  duration = duration || 3500;

  var zone = document.getElementById('toast-zone');
  if (!zone) return;

  var iconSvg = TOAST_ICONS[icon] || TOAST_ICONS[type] || TOAST_ICONS.info;

  var t = document.createElement('div');
  t.className = 'toast ' + type;
  t.setAttribute('role', 'alert');
  t.style.setProperty('--toast-duration', duration + 'ms');
  t.innerHTML =
    '<div class="toast-icon">' + iconSvg + '</div>' +
    '<div class="toast-msg">' + esc(msg) + '</div>' +
    '<button class="toast-close" onclick="this.parentElement.remove()" aria-label="Dismiss">' +
      '<svg viewBox="0 0 24 24"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg>' +
    '</button>';

  zone.appendChild(t);

  setTimeout(function() {
    t.classList.add('hide');
    setTimeout(function() { if (t.parentElement) t.remove(); }, 400);
  }, duration);
}

